# LIST METHODS

# Example 1 - append, insert, remove

nums = [1, 2, 3]

nums.append(4)        # add at end
nums.insert(1, 10)    # insert at index
nums.remove(2)        # remove value

print("Updated List:", nums)


# Example 2 - sort, reverse, pop

numbers = [5, 2, 8, 1]

numbers.sort()
print("Sorted:", numbers)

numbers.reverse()
print("Reversed:", numbers)

numbers.pop()
print("After Pop:", numbers)